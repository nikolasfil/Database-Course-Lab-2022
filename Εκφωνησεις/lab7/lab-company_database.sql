BEGIN TRANSACTION;
DROP TABLE IF EXISTS "sales";
CREATE TABLE IF NOT EXISTS "sales" (
	"salesid"	int NOT NULL DEFAULT '0',
	"salesdate"	date DEFAULT NULL,
	"salesperson"	varchar(6) DEFAULT NULL,
	"region"	varchar(15) DEFAULT NULL,
	CONSTRAINT "sales_employee_FK" FOREIGN KEY("salesperson") REFERENCES "employee"("empno") ON UPDATE CASCADE,
	PRIMARY KEY("salesid")
);
DROP TABLE IF EXISTS "project";
CREATE TABLE IF NOT EXISTS "project" (
	"projno"	varchar(6) NOT NULL DEFAULT '',
	"projname"	varchar(24) NOT NULL DEFAULT '',
	"deptno"	char(3) NOT NULL DEFAULT '',
	"resemp"	varchar(6) NOT NULL DEFAULT '',
	"prstdate"	date DEFAULT NULL,
	"prendate"	date DEFAULT NULL,
	"majproj"	varchar(6) DEFAULT NULL,
	CONSTRAINT "project_employee_FK" FOREIGN KEY("resemp") REFERENCES "employee"("empno"),
	CONSTRAINT "project_department_FK" FOREIGN KEY("deptno") REFERENCES "department"("deptno"),
	CONSTRAINT "project_project_FK" FOREIGN KEY("majproj") REFERENCES "project"("projno"),
	PRIMARY KEY("projno")
);
DROP TABLE IF EXISTS "employee";
CREATE TABLE IF NOT EXISTS "employee" (
	"empno"	varchar(6) NOT NULL DEFAULT '',
	"firstname"	varchar(12) NOT NULL DEFAULT '',
	"midinit"	char(1) NOT NULL DEFAULT '',
	"lastname"	varchar(15) NOT NULL DEFAULT '',
	"workdept"	char(3) DEFAULT NULL,
	"phoneno"	varchar(4) DEFAULT NULL,
	"hiredate"	date DEFAULT NULL,
	"job"	varchar(20) DEFAULT NULL,
	"edlevel"	smallint NOT NULL DEFAULT '0',
	"sex"	char(1) DEFAULT NULL,
	"birthdate"	date DEFAULT NULL,
	"salary"	int DEFAULT NULL,
	"bonus"	int DEFAULT NULL,
	"comm"	int DEFAULT NULL,
	CONSTRAINT "employee_department_FK" FOREIGN KEY("workdept") REFERENCES "department"("deptno") ON DELETE SET NULL,
	PRIMARY KEY("empno")
);
DROP TABLE IF EXISTS "emp_act";
CREATE TABLE IF NOT EXISTS "emp_act" (
	"empno"	char(6) NOT NULL DEFAULT '',
	"projno"	char(6) NOT NULL DEFAULT '',
	"emptime"	int DEFAULT NULL,
	"emstdate"	date NOT NULL DEFAULT '0000-00-00',
	"emenddate"	date DEFAULT NULL,
	CONSTRAINT "emp_act_project_FK" FOREIGN KEY("projno") REFERENCES "project"("projno") ON UPDATE CASCADE,
	CONSTRAINT "emp_act_employee_FK" FOREIGN KEY("empno") REFERENCES "employee"("empno") ON UPDATE CASCADE,
	PRIMARY KEY("empno","projno","emstdate")
);
DROP TABLE IF EXISTS "department";
CREATE TABLE IF NOT EXISTS "department" (
	"deptno"	char(3) NOT NULL DEFAULT '',
	"deptname"	varchar(29) NOT NULL DEFAULT '',
	"mgrno"	varchar(6) DEFAULT NULL,
	"admrdept"	char(3) NOT NULL DEFAULT '',
	"location"	varchar(16) DEFAULT NULL,
	CONSTRAINT "department_employee_FK" FOREIGN KEY("mgrno") REFERENCES "employee"("empno") ON DELETE SET NULL ON UPDATE CASCADE,
	CONSTRAINT "department_department_FK" FOREIGN KEY("admrdept") REFERENCES "department"("deptno"),
	PRIMARY KEY("deptno")
);
